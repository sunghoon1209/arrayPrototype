// arr.slice(start, end)
// start : 복사를 시작할 인덱스 ( 음수인 경우 배열의 끝에서의 인덱스)
// end : 복사를 종료할 인덱스 (해당하는 요소는 복사하지 않음, 생략가능하며 기본값은 length 프로퍼티)
